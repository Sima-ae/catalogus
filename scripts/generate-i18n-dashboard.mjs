function resolvePack(locale) {
  const dedicated = PACKS[locale]
  if (dedicated) return { ...PACKS.en, ...dedicated }
  const fallback = LOCALE_FALLBACK[locale]
  if (fallback && PACKS[fallback]) return { ...PACKS.en, ...PACKS[fallback] }
  return PACKS.en
}

const keys = Object.keys(PACKS.en)
let out = "import type { Locale } from '@/lib/i18n-locale-registry'\n\n"
out += 'export type DashboardMessageKey =\n'
keys.forEach((k, i) => {
  out += `  | '${k}'${i < keys.length - 1 ? '\n' : '\n\n'}`
})
out += 'type DashboardMessages = Record<DashboardMessageKey, string>\n\n'
out += 'const EN: DashboardMessages = '
out += JSON.stringify(PACKS.en, null, 2)
out += '\n\nconst BY_LOCALE: Partial<Record<Locale, DashboardMessages>> = {\n  en: EN,\n'

for (const loc of LOCALES) {
  if (loc === 'en') continue
  const full = resolvePack(loc)
  out += `  ${loc}: ${JSON.stringify(full)},\n`
}

out += `}\n\nexport function getDashboardMessages(locale: Locale): DashboardMessages {\n  return BY_LOCALE[locale] ?? EN\n}\n`

writeFileSync('src/lib/i18n-dashboard.ts', out)
console.log(`Generated i18n-dashboard.ts (${keys.length} keys × ${LOCALES.length} locales)`)
