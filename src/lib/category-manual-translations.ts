function packForCategory(categoryName: string): LocaleTranslationPack | null {
  const slug = slugifyCategory(categoryName)
  if (!slug) return null
  const resolved = PACK_SLUG_ALIASES[slug] ?? slug
  return PACKS[resolved] ?? null
}

/** Curated label for a category locale, or null when no manual pack exists. */
export function getCategoryManualTranslation(
  categoryName: string,
  locale: Locale
): string | null {
  const pack = packForCategory(categoryName)
  if (!pack) return null
  return packLabel(pack, locale)
}

/** `category.*` keys merged into static i18n bundles. */
export function getCategoryManualI18nExtras(locale: Locale): Record<string, string> {
  const out: Record<string, string> = {}
  for (const [slug, pack] of Object.entries(PACKS)) {
    const label = packLabel(pack, locale)
    if (!label) continue
    out[categoryI18nKey(pack.en)] = label
    out[`category.${slug}`] = label
  }
  for (const [alias, target] of Object.entries(PACK_SLUG_ALIASES)) {
    const pack = PACKS[target]
    if (!pack) continue
    out[`category.${alias}`] = packLabel(pack, locale)
  }
  return out
}
