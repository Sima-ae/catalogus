function packLabel(pack: LocaleTranslationPack, locale: Locale): string {
  if (pack[locale]) return pack[locale]!
  const fallback = LOCALE_FALLBACK[locale]
  if (fallback && pack[fallback]) return pack[fallback]!
  return pack.en
}

